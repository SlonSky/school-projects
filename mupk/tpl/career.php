    <h1>Система профорієнтаційної роботи Болградського міжшкільного навчально-виробничого комбінату</h1>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;У Болградському міжшкільному навчально-виробничому комбінаті профорієнтаційна робота проходить в два етапи:</p>
        <ul>
            <li>
                1 етап - це знайомство учнів шкіл з діяльністю Болградського міжшкільного навчально-виробничого комбінату. Проводиться у вигляді бесіди.
            </li><br>
            <li>
                2 етап - безпосередньо профорієнтація.
            </li>
        </ul>
        <p> &nbsp;&nbsp;&nbsp;&nbsp;Профорієнтація - це науково-практична система підготовки учнів до вільного і свідомого вибору професії.
            Мета профорієнтації - своєчасно підтримувати і стимулювати розвиток тих особливостей особистості, які служать передумовою майбутньої
            успішної професійної діяльності, сприяють пізнанню навколишнього світу, самопізнання, формування адекватної самооцінки, всебічному розвитку особистості.
    </p>
