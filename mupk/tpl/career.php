    <div class="text">
        <p>&nbsp;&nbsp;&nbsp;&nbsp;Профорієнтація включає в себе такі компоненти:</p>
        <ol>
            <li>Професійне просвітництво (профінформація);</li>
            <li>Професіографія;</li>
            <li>Профдіагностика;</li>
            <li>Профвідбор.</li>
        </ol>

        <p>1. Професійне просвітництво (профінформації) - озброєння учнів певною сукупністю знань про соціально-економічних і психофізіологічних умовах вибору професії.</p>
        <p>2. Професіографія - опис професій або спеціальностей, що включає в себе основні вимоги, які вони висувають до психологічних і фізичних якостей людини, а також фактори, які зумовлюють успішність або неуспішно, задоволеність або незадоволеність особистості даної професійною діяльністю.</p>
        <p>3. Профдіагностика - дослідження професійних інтересів, нахилів, психофізіологічних і особистісних особливостей учнів за допомогою психодіагностичних тестів.</p>
        <p>4. Профвідтбір - визначення для особистості професії або спеціальності (у даному випадку – профілю навчання в МНВК), спираючись на виявлені психофізіологічні та особистісні якості людини.</p>
    </div>
</section>
